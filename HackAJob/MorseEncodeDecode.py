CODE_REVERSED = {value: key for key, value in CODE.items()}

def solve( morseToEnglish, textToTranslate):
    translatedText = " "
    if (morseToEnglish):
        translatedText =  ''.join(str(CODE_REVERSED.get(i)) for i in textToTranslate.split())

    else:
        translatedText = ' '.join(str(CODE.get(i.upper())) for i in textToTranslate)

    return translatedText

